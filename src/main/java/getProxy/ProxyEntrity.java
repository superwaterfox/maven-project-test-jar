package getProxy;

public class ProxyEntrity {
	private String proxyIP;
	private Integer proxyPORT;
	private Double proxySpeed;
	private String proxyArea;

	public String getProxyIP() {
		return proxyIP;
	}

	public void setProxyIP(String proxyIP) {
		this.proxyIP = proxyIP;
	}

	public Integer getProxyPORT() {
		return proxyPORT;
	}

	public void setProxyPORT(Integer proxyPORT) {
		this.proxyPORT = proxyPORT;
	} 

	public Double getProxySpeed() {
		return proxySpeed;
	}

	public void setProxySpeed(Double proxySpeed) {
		this.proxySpeed = proxySpeed;
	}

	public String getProxyArea() {
		return proxyArea;
	}

	public void setProxyArea(String proxyArea) {
		this.proxyArea = proxyArea;
	}

}
